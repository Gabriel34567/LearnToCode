#XIV-B. 巢狀迴圈

#巢狀迴圈是指迴圈內有迴圈

#XIV-B1. 巢狀迴圈的表示方法
for row in xiv_a1plus: #迴圈1. 把多行列表分成一個個一維列表
    for col in row: #迴圈2. 再把每個一維列表分成一個個值
        print(col)

