#XIV. 二維列表 & 巢狀迴圈

#XIV-A. 二維列表

#回顧：列表

d = ["I","D","K"] #列表是可以存放很多個值的資料型態

#但是...這只是一個1D(一維)的列表，怎樣可以把列表變成2D(二維)呢？

#XIV-A1. 維度提升
xiv_a1 = [["A","B","C"],["D","E","F"],["G","H","I"]]
#為了讓二維列表更有感覺，可以寫成下面的形式：
xiv_a1plus = [
    ["A","B","C"], #第0行 (Row0)
    ["D","E","F"], #第1行 (Row1)
    ["G","H","I"]  #第2行 (Row2)
] #每行有3列 (3 Columns) (也是從0算起！)

#XIV-A2. 取值
#XIV-A201. 第0行，第0列
print(xiv_a1plus[0][0])
#XIV-A202. 第1行，第2列
print(xiv_a1plus[1][2])

